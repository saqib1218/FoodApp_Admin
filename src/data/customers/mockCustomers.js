// Helper function to get customer by ID
export const getCustomerById = (customerId) => {
  return mockCustomers.find(customer => customer.id === customerId) || null;
};

// Helper function to get customers by city
export const getCustomersByCity = (city) => {
  return mockCustomers.filter(customer => customer.city === city);
};

// Helper function to get customers by status
export const getCustomersByStatus = (status) => {
  return mockCustomers.filter(customer => customer.status === status);
};

// Get unique cities from customers
export const getCustomerCities = () => {
  return [...new Set(mockCustomers.map(customer => customer.city))];
};

// Get unique statuses from customers
export const getCustomerStatuses = () => {
  return [...new Set(mockCustomers.map(customer => customer.status))];
};
